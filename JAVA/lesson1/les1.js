// В однакових виразах не використовувати однакові оператори!!!

// 5 ? 6 -> true
console.log (5<6)

// 5 ? 6 -> false
console.log (5>6)

// 5 ? 6 -> false
console.log (5==6)

// 5 ? 6 -> false
console.log(5>=6)

// 10 ? 10 -> true
console.log(10===10)

// 10 ? 10 -> true
console.log(10>=10)

// 10 ? 10 -> false
console.log(10<10)

// 10 ? 10 -> false
console.log(10!=10)

// 10 ? 10 -> false
console.log(10!==10)

// 123 ? '123' -> false
console.log(123==='123')

// 123 ? '123' -> true
console.log(123!=='123')